
public class TVProgram {
	private String name ;
	private int season ;
	private int episode ;
	private String airtime ;
	private int viewers ;

	public TVProgram(String a, int b, int c, String d, int e){
		name = a ;
		season = b ;
		episode = c ;
		airtime = d ;
		viewers = e;
	}
	
}
