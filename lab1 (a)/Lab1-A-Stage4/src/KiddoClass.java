
public class KiddoClass {
	private String type;
	private String level;
	private String instructor;
	
	public KiddoClass(String a, String b, String c){
		type = a;
		level = b;
		instructor = c;
	}
	
	public String getType(){
		return type;
	}
	public String getLevel(){
		return level;
	}
	public String getInstructor(){
		return instructor;
	}

}
